let month = 4;

switch (month) {
    case 1:
        console.log('Month is January');
        break;

    case 2:
        console.log('Month is February');
        break;

    case 3:
        console.log('Month is March');
        break;

    case 4:
        console.log('Month is April');
        break;

    case 5:
        console.log('Month is May');
        break;

    case 12:
        console.log('Month is December');
        break;
}
